                                   <p class=" pt-3">
                                    In todays blog, i am going to be showing you a pratical way that you can download, install xammp server and also how you can run it on your windows computer. just incase you can read this blog till the end, we have also put up a video on the step by step way to start and run your xammp localhost
                                   </p>
                                   

                                   <p><b>Xammp</b> is the most popular software package which is used to set up a PHP development environment for web services by providing all the required software components. During the process of software deployment, most of the web servers use almost similar components, so use of <b>Xammp</b> provides easy transition from local server to live server. <b>Xammp</b> is a <b>Amp</b> stack which stands for <b> Cross platform, Apache, MySQL, PHP, perl</b> with some additional administrative software tools such as PHPMyAdmin (for database access), FileZilla FTP server, Mercury mail server and JSP Tomcat server. </p>
                                   <p>The <b>XAMPP</b> server is used to test PHP pages. It works as local server. It contains <b> MySQL database</b> to manage or save data on a local server.</p>


                                   <h5 class="pb-3 pt-3">Steps to install XAMPP on Windows: </h5>

                                   <div>
                                       <ul class="pb-3">
                                           <li>In the web browser, visit <a href="https://www.apachefriends.org/index.html" target="_blank" class="text-success fw-bold">Apache Friends</a> and download XAMPP installer. </li>
                                    
                                       </ul>
                                       <img src="img/xammp-2.jpg" alt="" width="100%">
                                   </div>
                                   <div>
                                       <ul class="py-4">
                                           <li>During the installation process, select the required components like MySQL, FileZilla ftp server, PHP, phpMyAdmin or leave the default options and click the Next button. </li>
                                       </ul>
                                       <img src="img/xammp-3.jpg" alt="" height="250px" width="100%">
                                   </div>

                                   <div>
                                       <ul class="py-4">
                                           <li>Uncheck the Learn more about bitnami option and click Next button.</li>

                                           <li>Choose the root directory path to set up the htdocs folder for our applications. For example ‘C:\xampp’.</li>

                                           <li>Click the Allow access button to allow the XAMPP modules from the Windows firewall.</li>

                                           <li>After the installation process, click the Finish button of the XAMPP Setup wizard.</li>

                                           <li>Now the XAMPP icon is clearly visible on the right side of start menu. Show or Hide can be set by using the control panel by clicking on the icon.</li>

                                           <li>To start Apache and MySql, just click on the Start button on the control panel.</li>
                                       </ul>
                                       <img src="img/xammp-4.jpg" alt="" width="100%">
                                   </div>

                                   <p class="pt-4"><b>Note:</b> Suppose Apache is not starting, it means some other service is running at port <b>80</b>. In this case, stop the other service temporarily and restart it. <b>Making server request: </b>
                                    Open your web browser and check whether the XAMPP service has properly installed or not. Type in the <b>URL: http://localhost.</b> If you are able to see the default page for XAMPP, you have successfully installed your XAMPP Server.</p>

                                    <p><b>To Check if PHP is Working: </b>  All the website related files are organized in a folder called <b>htdocs</b> and then run index.php file by using <b>http://localhost/index.php </b> or <b>http://localhost.</b></p>

                                    <p><b>Note:</b> For every new website or application, its always better to create a different folder inside <b>htdocs</b>, to keep it organized and avoid confusion.
                                        For example, if we create a folder geeksforgeeks and then create a file nameed as <b>‘helloWorld.php’</b>. All the contents related to it are put inside the folder ‘geeksforgeeks’. So the root ‘URL’ of the website will be <b>‘http://localhost/geeksforgeeks/’</b>. So any home page is accessed by typing the root URL in the browser. To see the output, just type <b>‘http://localhost/geeksforgeeks/helloWorld.php’</b>.</p>
